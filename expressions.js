/**
 * Just like the parts of speech in the English language,
 * JS has a structures too:
 * In JavaScript, code is typically written as a sequence of statements.
 * A statement is a complete instruction that performs a specific action.
 * For example, a variable declaration, a function call, or a loop are all examples of statements.
 *
 * Expressions, on the other hand, are smaller units within a statement that produce a value.
 * An expression can consist of operators, operands, and other expressions.
 * Expressions can be as simple as a single variable or value, or they can be more complex, involving multiple operators and operands.
 *
 * Operators in JavaScript are symbols or keywords.
 * They perform specific operations on one or more operands to produce results.
 * Examples include arithmetic operators (+, -, *, /), comparison operators (>, <, ===), and logical operators (&&, ||).
 *
 * Operands, are the values or variables that operators act upon. They can be literal values (such as numbers or strings)
 * or variables that hold values.
 *
 * Therefore, it would be more accurate to say that operators are symbols or keywords that perform operations, and
 * operands are the values or variables that are operated on by the operators.
 *
 * In summary, JavaScript code is structured as a sequence of statements, and statements are composed of expressions.
 * Expressions, in turn, consist of operators and operands (values or variables). This structure allows you to write code
 * that performs various operations and manipulations.
 */



